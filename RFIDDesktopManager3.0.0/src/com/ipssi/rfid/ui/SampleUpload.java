    private void searchButtonAction() {
        boolean destroyIt = false;
        Connection conn = null;
        pair=null;       
        if((lotId.getText() == null || lotId.getText().trim().length() == 0) && (lotName.getText() == null || lotName.getText().trim().length() == 0)){
            JOptionPane.showMessageDialog(null, "Please Search by lot Id or Lot Name.");
            return;
        }else if (lotId.getText() != null && lotId.getText().length()>0 && !Utils.isNumericDigit(lotId.getText())) {
				JOptionPane.showMessageDialog(null, "Please Enter Valid Numeric Lot-Id");
				lotId.setText("");
				lotId.requestFocusInWindow();
				return;
        } else {
			try {
				conn = DBConnectionPool.getConnectionFromPoolNonWeb();
				pair = GateInDao.searchPostLotData(conn,lotId.getText(), lotName.getText());
				if (pair != null) {
					uploadFile.setVisible(true);
					msgDisplay.setText("Tag Scanned Please Upload File");
					lotId.setText(pair.first + "");
					lotName.setText(pair.second);
					DropDownValues.setComboItem(labDropDownList, pair.third);
				} else {
					JOptionPane.showMessageDialog(null, "Please Scan Tag.");
					clearInputs();
					msgDisplay.setText("");
				}
			} catch (Exception ex) {
				JOptionPane.showMessageDialog(null,
						UIConstant.SAVE_FAILER_MESSAGE);
				ex.printStackTrace();
				destroyIt = true;
			} finally {
				try {
					DBConnectionPool.returnConnectionToPoolNonWeb(conn,
							destroyIt);
				} catch (Exception ex) {
					ex.printStackTrace();
				}
			}
		}
	}

