    private void setLabList() {
		Connection conn = null;
		boolean destroyIt = false;
		try{
			conn = DBConnectionPool.getConnectionFromPoolNonWeb();
			labDropDownList.removeAllItems();
			labDropDownList.getItemCount();
			labDropDownList.addItem(new ComboItem(Misc.getUndefInt(), "select"));
			ArrayList<ComboItem> minesList = DropDownValues.getLabList(conn);
			for (int i = 0; i < minesList.size(); i++) {
				labDropDownList.addItem(minesList.get(i));
			}
			labDropDownList.setSelectedIndex(0);
		}catch(Exception ex){
			ex.printStackTrace();
			destroyIt = true;
		}finally{
			try{
				DBConnectionPool.returnConnectionToPoolNonWeb(conn, destroyIt);
			}catch(Exception ex){
				ex.printStackTrace();
			}
		}
	}


